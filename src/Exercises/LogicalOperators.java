package Exercises;

public class LogicalOperators {


    public static void main(String[] args) {

        boolean b1 = false;

        boolean b2 = true;


        //// ve &&
        System.out.println("b1 && b2 : " + (b1&&b2));


        //// veya ||
        System.out.println("b1 || b2 : " + (b1||b2));




    }


    /////   true && true ---->> true
    //// true || true ----->>> true
    //// true && false ----->>> false






}
